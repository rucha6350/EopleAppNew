package rucha.sawant.eopleapp.Model;

public class Data {

    private String cost, typedata;

    public Data(String cost, String typedata) {
        this.cost = cost;
        this.typedata = typedata;
    }

    public String getCost() {
        return cost;
    }

    public String getTypedata() {
        return typedata;
    }
}
